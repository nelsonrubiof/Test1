package com.scopix.periscope.ffmpeg;

public class VTTObject {

    private String date;
    private String store;
    private String endPath;
    private String duration;
    private String clientName;
    private String videoBaseName;
    private String spritesScaleX;
    private String spritesScaleY;
    private String operatorImgUrl;

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the store
     */
    public String getStore() {
        return store;
    }

    /**
     * @param store the store to set
     */
    public void setStore(String store) {
        this.store = store;
    }

    /**
     * @return the endPath
     */
    public String getEndPath() {
        return endPath;
    }

    /**
     * @param endPath the endPath to set
     */
    public void setEndPath(String endPath) {
        this.endPath = endPath;
    }

    /**
     * @return the duration
     */
    public String getDuration() {
        return duration;
    }

    /**
     * @param duration the duration to set
     */
    public void setDuration(String duration) {
        this.duration = duration;
    }

    /**
     * @return the clientName
     */
    public String getClientName() {
        return clientName;
    }

    /**
     * @param clientName the clientName to set
     */
    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    /**
     * @return the videoBaseName
     */
    public String getVideoBaseName() {
        return videoBaseName;
    }

    /**
     * @param videoBaseName the videoBaseName to set
     */
    public void setVideoBaseName(String videoBaseName) {
        this.videoBaseName = videoBaseName;
    }

    /**
     * @return the spritesScaleX
     */
    public String getSpritesScaleX() {
        return spritesScaleX;
    }

    /**
     * @param spritesScaleX the spritesScaleX to set
     */
    public void setSpritesScaleX(String spritesScaleX) {
        this.spritesScaleX = spritesScaleX;
    }

    /**
     * @return the spritesScaleY
     */
    public String getSpritesScaleY() {
        return spritesScaleY;
    }

    /**
     * @param spritesScaleY the spritesScaleY to set
     */
    public void setSpritesScaleY(String spritesScaleY) {
        this.spritesScaleY = spritesScaleY;
    }

    /**
     * @return the operatorImgUrl
     */
    public String getOperatorImgUrl() {
        return operatorImgUrl;
    }

    /**
     * @param operatorImgUrl the operatorImgUrl to set
     */
    public void setOperatorImgUrl(String operatorImgUrl) {
        this.operatorImgUrl = operatorImgUrl;
    }
}