package com.scopix.periscope.extractionplanmanagement.commands;

import com.scopix.periscope.extractionplanmanagement.ExtractionPlanCustomizing;
import com.scopix.periscope.periscopefoundation.persistence.GenericDAO;
import com.scopix.periscope.periscopefoundation.util.config.HibernateSupport;

/**
 *
 * @author nelson
 */
public class GetExtractionPlanCustomizingByIdCommand {

    private GenericDAO dao;

    public ExtractionPlanCustomizing execute(Integer id) {
        ExtractionPlanCustomizing epc = getDao().get(id, ExtractionPlanCustomizing.class);
        return epc;
    }

    public GenericDAO getDao() {
        if (dao == null) {
            dao = HibernateSupport.getInstance().findGenericDAO();
        }
        return dao;
    }

    public void setDao(GenericDAO dao) {
        this.dao = dao;
    }
}
